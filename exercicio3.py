# Sequência a)
sequencia_a = [1, 3, 5, 7]
proximo_elemento_a = sequencia_a[-1] + 2
print("Próximo elemento da sequência a):", proximo_elemento_a)

# Sequência b)
sequencia_b = [2, 4, 8, 16, 32, 64]
proximo_elemento_b = sequencia_b[-1] * 2
print("Próximo elemento da sequência b):", proximo_elemento_b)

# Sequência c)
sequencia_c = [0, 1, 4, 9, 16, 25, 36]
indice_proximo_elemento_c = len(sequencia_c)
proximo_elemento_c = indice_proximo_elemento_c ** 2
print("Próximo elemento da sequência c):", proximo_elemento_c)

# Sequência d)
sequencia_d = [4, 16, 36, 64]
ultimo_elemento_d = sequencia_d[-1]
raiz_quadrada_d = int(ultimo_elemento_d ** 0.5)
proximo_elemento_d = (raiz_quadrada_d + 2) ** 2
print("Próximo elemento da sequência d):", proximo_elemento_d)

# Sequência e)
ultimo_elemento_e = 8
penultimo_elemento_e = 5
proximo_elemento_e = ultimo_elemento_e + penultimo_elemento_e
print("Próximo elemento da sequência e):", proximo_elemento_e)

# Sequência f)
sequencia_f = [2, 10, 12, 16, 17, 18, 19]
proximo_elemento_f = sequencia_f[-1] + 1
print("Próximo elemento da sequência f):", proximo_elemento_f)
